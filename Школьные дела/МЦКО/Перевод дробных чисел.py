# Перевод дробных чисел
# 1010.1 (2) -> (10)
print(int('1010', 2) + int('1', 2) / 2 ** 1) # 10.5
# 1010.001 (2) -> (10)
print(int('1010', 2) + int('001', 2) / 2 ** 3) # 10.125
# 1010.1010 (2) -> (10)
print(int('1010', 2) + int('1010', 2) / 2 ** 4) # 10.625

# 11.4 (8) -> (10)
print(int('11', 8) + int('4', 8) / 8 ** 1) # 9.5
# 11.047 (8) -> (10)
print(int('11', 8) + int('047', 8) / 8 ** 3) # 9.07617188

